    private void tbl_HoaDonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_HoaDonMouseClicked
        // TODO add your handling code here:
        if (SwingUtilities.isRightMouseButton(evt)) {
            int row = tbl_HoaDon.rowAtPoint(evt.getPoint());
            if (row >= 0) {
                tbl_HoaDon.setRowSelectionInterval(row, row);
//                popupHDCt.show(evt.getComponent(), evt.getX(), evt.getY());
            }
        } else {
            int selectedRow = tbl_HoaDon.getSelectedRow();
            txtMaHD.setText(tbl_HoaDon.getValueAt(selectedRow, 0).toString().toString());
            txtKhachHang.setText(tbl_HoaDon.getValueAt(selectedRow, 1).toString());
            txtTienThanhToan.setText(tbl_HoaDon.getValueAt(selectedRow, 3).toString());
            String idHoaDon = _lstHoaDon.get(selectedRow).getId();
            fillTableHDCT(idHoaDon);
//            ArrayList<HDCT_CTSP> hdctsp = hoaDonChiTietRepository.getByIdHoaDon(idHoaDon);
//            if (selectedRow < 0 || selectedRow > hdctsp.size()) {
//                return;
//            }

        }
    }//GEN-LAST:event_tbl_HoaDonMouseClicked

