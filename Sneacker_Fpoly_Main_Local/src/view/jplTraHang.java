    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        try {
            int row = tblHoaDonChiTiet.getSelectedRow();
            if (row == -1) {
                return;
            } else {
                Date ngayTraHang = sdf.parse(sdf.format(ngayHienTai));

                // Đặt giá trị cho txtNgayTraHang
//                txtNgayTraHang.setText(sdf.format(ngayHienTai));
                txtNgayTraHang.setDate(ngayTraHang);
                txtTongTienHoanTra.setText(tblHoaDonChiTiet.getValueAt(row, 3).toString());
//                fillTableHT();
                String idHoaDon = _lstHoaDon.get(row).getId();
                fillTableHT(idHoaDon);
            }
        } catch (Exception e) {
        }

    }//GEN-LAST:event_jButton1MouseClicked

